package com.example.introductiontose;

/**
 * Lớp Main là lớp chính để khởi chạy ứng dụng.
 * Nó chứa phương thức main() để bắt đầu quá trình thực thi ứng dụng.
 */
public class Main {
    /**
     * Phương thức main() là điểm bắt đầu của ứng dụng.
     *
     * @param args Tham số dòng lệnh được truyền vào khi chạy ứng dụng.
     */
    public static void main(String[] args) {
        // Gọi phương thức main() của lớp Application để bắt đầu ứng dụng JavaFX
        Application.main(args);
    }
}
